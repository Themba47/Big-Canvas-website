						<h2>Range by David Epstein<br><p class="underline"></p></h2>
						
						<p>Just recently read the book "Range" by David Epstein. In the book, his main idea is that experimenting with a lot of fields and having a broad idea of things is essential to being great and that hyperspecialization is the wrong way to go about things. At the beginning of the book, he uses Tiger Woods and Roger Feder as examples as one specialized early (Tiger Woods) and the other indulged in different sports before settling in tennis (Roger Feder).<br><br> His idea is that hyper specialists are limited and that their greatness in their field is also their biggest weakness because they have spent their whole life in one particular field, they are masters in their field, they understand and see intricacies that we normal people can't see and thus because they have only indulged in one field their whole life they struggle to embark in other fields and do other things that are outside their domain of expertise and this is dangerous in the 21st century because of how the world is changing and innovating so rapidly, he has a term for this world and it is "Wicked World".<br> A wicked world by his definition is a world that is constantly changing, a world where 2 professional projects are never the same, two work environments are never the same basically, a world where humans do not do repetitive work but are forced to do creative work or work that requires high IQ and problem-solving skills. What he suggests is that we generalize and do a lot of things before deciding to specialize. The point of doing this is to broaden one's knowledge of the world, broaden one's understanding of oneself and most importantly learn how to learn. This is the key thing the Author is trying to teach in this book: Learning how to learn. 
						By trying a lot of things and doing different activities, you do fall behind people that specialize but what you gain is you start to see patterns in how to learn.<br> This is extremely helpful in a "Wicked world" because you know how to adapt, evolve and adjust quickly to any environment. People who hyperspecialize leave themselves open to being outdated or out of fashion. Another reason to generalize first is that generalists have an easier time finding correlations between different fields thus they are more prone to finding solutions because they can take an idea or solution from one field and implement it in another. He highlights a couple of examples where individuals were able to find solutions to big problems by looking outside their field.</p>

						<h2>KEY TAKEAWAYS</h2>
						<p class="underline"></p>
						<p>My key takeaway is the concept of "Learning how to learn". That is an important skill to master, being able to adjust and learn quickly gives you a huge advantage in this ever evolving world. Also, the idea that most great ideas or solutions come from unlikely sources or places thus it is important that even if you are a hyper specialist, that you also do other activities outside your field of expertise.  But also for those who are still searching for their "thing or passion" its important to do, take action, try something, it's not about getting anywhere but it's really about the process of understanding yourself. We as humans change so what you like now might change with time and that is completely alright. The last takeaway and I think this one is specific to parents is that experimenting a lot does not mean you won't be great or that your child won't be great, on the contrary, it will help them in the long run cause they will understand themselves better and will also be able to be great in more than one field because they know how to learn.
