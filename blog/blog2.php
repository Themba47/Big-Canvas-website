						<h1>5 websites every creative should know about<br><label class="thedag">25 June 2020</label><br><p class="underline"></p></h1><br>
						
						
						<p>As a creative you find yourself working through projects and needing certain things to make the work distinctive, whether it's an image for a poster or an icon for an app, you always in need of some sort of graphics. As a creative myself, I understand that we all love to create but sometimes you will find yourself doing a project where you don't have the time to create icons or worse host a whole photoshoot, sometimes you just need those things now, that is when most people turn to google images. the problem with google images is quality. Yes, you can find whatever image or icon you need but most of the time the quality will be poor. Fortunately, there are websites dedicated to providing FREE, HIGH QUALITY design tools like; images, icons, fonts, and more. There is a good deal of stock websites but in this blog, I will be sharing 5 of my go-to sites and what each provides.</p>

						<a href="https://www.unsplash.com"><h2>1. Unsplash.com</h2></a>
						<p class="underline"></p>
						<p>Unsplash is a website that provides free, high-quality stock images. They have images for everything, whether its cityscapes, people working on their laptops, cars or nature, they've got it all. These images are provided by a community of photographers from all walks of life.
						</p><br>

						<a href="https://www.fontawesome.com"><h2>2. Fontawesome.com</h2></a>
						<p class="underline"></p>
						<p>Fontawesome is a website that provides a library of icons. As a creator, you often find yourself using icons, especially if you deal with web design so you don't want to reinvent the wheel by creating the same icons, Thus Fontawesome is the best place to find whatever icon you need. The key factor with Fontawesome is that not all of their icons are free. The common ones are free like; home icons, arrows, and social media icons but they also have special designs if you looking to make your website unique, but in order to access these special icons you need to pay for their premium icons.
						</p><br>

						<a href="https://www.fonts.google.com"><h2>3. Fonts.google.com</h2></a>
						<p class="underline"></p>
						<p>Fonts.google.com is a Google platform that provides free fonts. I don't know about you but I do not like to use the standard fonts that are on the computer like; Arial, Times, and Cooper Black. I like my work to look unique so I like to use special fonts. With Google fonts, you can download the font to use in photoshop or if you need a font for your website, you can just copy and attach the link of the font on your website, so your website requests that font. 
						</p><br>

						<a href="https://www.behance.net"><h2>4. Behance.net</h2></a>
						<p class="underline"></p>
						<p>Behance is an amazing platform. It is a platform where you will find portfolios of creatives of all types. I like to use Behance to find photoshop mockups to use for design projects. You can find all sorts of mockups from; wine bottles, t-shirts, bags, and more. The only thing I ask for is if you going to download from there, please credit the artists because they also trying to get somewhere and grow their brand and they could have easily refused to share their work on Behance and instead sold their work on their own private website.
						</p><br>

						<a href="https://www.pexels.com"><h2>5. Pexels.com</h2></a>
						<p class="underline"></p>
						<p>Pexels like Unsplash is also a stock image website, they do pretty much everything Unsplash does. The only difference is that Pexels has stock videos, a lot of stock videos. I guess Unsplash priorities images because they have a wide range of images while Pexels is limited in terms of variety, but they have a lot of stock videos.
						</p><br>

						<h2>Conclusion</h2>
						<p class="underline"></p>
						<p>These 5 websites are my go-to websites but there are a plethora of websites that provide similar things. But I hope for those starting out this has been helpful. Please also share your favourite websites so that we can have more websites to share in part 2 of this blog. Keep well and happy creating.
						</p><br>

